SET SESSION FOREIGN_KEY_CHECKS=0;


/* DROP TABLES */

DROP TABLE IF EXISTS T_WM_CUST_ADDRESS;




/* CREATE TABLES */
CREATE TABLE T_WM_CUST_ADDRESS
(
	F_ID VARCHAR(50) NOT NULL COMMENT 'F_ID',
	PROVINCE VARCHAR(50) COMMENT '省',
	CITY VARCHAR(50) COMMENT '市',
	ADDRESS VARCHAR(200) COMMENT '详细地址',
	PHONE VARCHAR(50) COMMENT '收货电话',
	POST VARCHAR(50) COMMENT '邮政编码',
	REMARK VARCHAR(100) COMMENT '说明',
	CUST_ID VARCHAR(50) COMMENT '客户编号',
	DEFAULT_ADDRESS VARCHAR(2) COMMENT '是否默认 : 1:为默认 0:不是',
	CONSIGNEE_PERSON varchar(50) COMMENT '收货人',
	PRIMARY KEY (F_ID)
) COMMENT = '客户地址信息表' DEFAULT CHARACTER SET UTF8 COLLATE UTF8_GENERAL_CI;


SET SESSION FOREIGN_KEY_CHECKS=1;
